  function updateValue() {
    const valueLuasWilayah = Number(luas_wilayah.value);
    const valueJumlahJiwa = Number(jumlah_jiwa.value);

    if(valueLuasWilayah && valueJumlahJiwa) {
      const valueKepadatanPenduduk = valueJumlahJiwa / valueLuasWilayah;
      kepadatan_penduduk.value = valueKepadatanPenduduk.toFixed(2);
      
      if(valueKepadatanPenduduk <= 150) {
        tingkat_kepadatan_penduduk.value = 'Rendah';
        kependudukan.value = 1;
      } else if(valueKepadatanPenduduk >= 151 && valueKepadatanPenduduk <=200) {
        tingkat_kepadatan_penduduk.value = 'Sedang';
        kependudukan.value = 3;
      } else if(valueKepadatanPenduduk >= 201 && valueKepadatanPenduduk <= 400) {
        tingkat_kepadatan_penduduk.value = 'Tinggi';
        kependudukan.value = 5;
      } else if(valueKepadatanPenduduk > 400) {
        kependudukan.value = 5;
        tingkat_kepadatan_penduduk.value = "Sangat Padat";
      }
    } else {
      alert("Harap pastikan kamu sudah mengisikan nilai jumlah penduduk dan luas wilayah")
    }
  }

